package type.primitive;

public class LongTest {

	public static void main(String[] args) {
		long milliSecond;
		
		milliSecond = 1000;
		System.out.println(milliSecond);
		milliSecond = 3000L;
		System.out.println(milliSecond);
		milliSecond = 5000l;
		System.out.println(milliSecond);
	}

}
